using UnityEngine;

public class PlayerNameController : MonoBehaviour
{
    public static PlayerNameController Instance;
    public string playerName;

    private void Awake()
    {
        if (Instance == null)
        {
            Instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
